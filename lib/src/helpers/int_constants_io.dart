/// Maximum value for a 64-bit [int]. Note that JavaScript can only represent up to 2^53 as the max integer value.
/// Therefore, this constant represents the maximum value a 64-bit integer can hold in Dart.
const int maxIntValue = 9223372036854775807;

/// Minimum value for a 64-bit [int]. Note that JavaScript can only represent down to -2^53 as the minimum integer value.
/// Thus, this constant represents the minimum value a 64-bit integer can hold in Dart.
const int minIntValue = -9223372036854775808;
